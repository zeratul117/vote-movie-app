'use client'
import { useState, useEffect } from 'react'
import React from 'react'
import { Media, Movie } from '@/payload-types'
import Image from 'next/image'
import { addVotes } from '@/movies'
import Link from 'next/link'

export default function MovieCards({movies: initialMovies}:  { movies: Movie[]}) {
    const [movies, setMovies] = useState<Movie[]>(initialMovies)
    const [showMax, setShowMax] = useState<{ [key: number]: boolean }>({})
    const [loading, setLoading] = useState<{ [key: number]: boolean }>({})

    async function addVotesLocal(movieId: number) {
        setLoading(prev => ({ ...prev, [movieId]: true }))
        const updatedMovies = await addVotes(movieId)
        if (updatedMovies.length > 0) {
            setShowMax(prev => ({ ...prev, [movieId]: false }))
        } else {
            setShowMax(prev => ({ ...prev, [movieId]: true }))
        }
        setLoading(prev => ({ ...prev, [movieId]: false }))
        return updatedMovies;
    }

    return (
        <div className="overflow-visible flex flex-wrap justify-center p-3 gap-4">
            {movies.map((movie) => (
                <div key={movie.id} className="max-w-75 p-4 rounded-sm border borderColor">
                    <Link href={`/movie/${movie.id}`}>
                        <Image
                        src={(movie.poster as Media)?.url ?? ''}
                        alt={(movie.poster as Media)?.text ?? ''}
                        width={(movie.poster as Media)?.width ?? 100}
                        height={(movie.poster as Media)?.height ?? 100}
                        />
                    </Link>
                    <div className="pt-3">
                        <div className="flex items-center justify-between">
                            <div className="textColor flex items-center">
                                <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="size-6">
                                    <path strokeLinecap="round" strokeLinejoin="round" d="M6.633 10.25c.806 0 1.533-.446 2.031-1.08a9.041 9.041 0 0 1 2.861-2.4c.723-.384 1.35-.956 1.653-1.715a4.498 4.498 0 0 0 .322-1.672V2.75a.75.75 0 0 1 .75-.75 2.25 2.25 0 0 1 2.25 2.25c0 1.152-.26 2.243-.723 3.218-.266.558.107 1.282.725 1.282m0 0h3.126c1.026 0 1.945.694 2.054 1.715.045.422.068.85.068 1.285a11.95 11.95 0 0 1-2.649 7.521c-.388.482-.987.729-1.605.729H13.48c-.483 0-.964-.078-1.423-.23l-3.114-1.04a4.501 4.501 0 0 0-1.423-.23H5.904m10.598-9.75H14.25M5.904 18.5c.083.205.173.405.27.602.197.4-.078.898-.523.898h-.908c-.889 0-1.713-.518-1.972-1.368a12 12 0 0 1-.521-3.507c0-1.553.295-3.036.831-4.398C3.387 9.953 4.167 9.5 5 9.5h1.053c.472 0 .745.556.5.96a8.958 8.958 0 0 0-1.302 4.665c0 1.194.232 2.333.654 3.375Z" />
                                </svg>
                                {showMax[movie.id] ? <p className='pl-1'>Max</p> : <p className='pl-1'>{movie.votes}</p>}
                            </div>
                            <button disabled={loading[movie.id]} onClick={async () => setMovies(await addVotesLocal(movie.id)) } type="button" className="py-2.5 px-5 mr-2 text-sm font-medium text-gray-900 bg-white rounded border border-gray-200 hover:bg-gray-100 hover:text-blue-700 focus:z-10 focus:ring-4 focus:outline-none focus:ring-blue-700 focus:text-blue-700 dark:bg-gray-800 dark:text-gray-400 dark:border-gray-600 dark:hover:text-white dark:hover:bg-gray-700 inline-flex items-center">
                                {loading[movie.id] ?                             <svg aria-hidden="true" role="status" className="inline mr-2 w-4 h-4 text-gray-200 animate-spin dark:text-gray-600" viewBox="0 0 100 101" fill="none" xmlns="http://www.w3.org/2000/svg">
                                <path d="M100 50.5908C100 78.2051 77.6142 100.591 50 100.591C22.3858 100.591 0 78.2051 0 50.5908C0 22.9766 22.3858 0.59082 50 0.59082C77.6142 0.59082 100 22.9766 100 50.5908ZM9.08144 50.5908C9.08144 73.1895 27.4013 91.5094 50 91.5094C72.5987 91.5094 90.9186 73.1895 90.9186 50.5908C90.9186 27.9921 72.5987 9.67226 50 9.67226C27.4013 9.67226 9.08144 27.9921 9.08144 50.5908Z" fill="currentColor"></path>
                                <path d="M93.9676 39.0409C96.393 38.4038 97.8624 35.9116 97.0079 33.5539C95.2932 28.8227 92.871 24.3692 89.8167 20.348C85.8452 15.1192 80.8826 10.7238 75.2124 7.41289C69.5422 4.10194 63.2754 1.94025 56.7698 1.05124C51.7666 0.367541 46.6976 0.446843 41.7345 1.27873C39.2613 1.69328 37.813 4.19778 38.4501 6.62326C39.0873 9.04874 41.5694 10.4717 44.0505 10.1071C47.8511 9.54855 51.7191 9.52689 55.5402 10.0491C60.8642 10.7766 65.9928 12.5457 70.6331 15.2552C75.2735 17.9648 79.3347 21.5619 82.5849 25.841C84.9175 28.9121 86.7997 32.2913 88.1811 35.8758C89.083 38.2158 91.5421 39.6781 93.9676 39.0409Z" fill="#1C64F2"></path>
                                </svg> : <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="size-5">
                                    <path strokeLinecap="round" strokeLinejoin="round" d="M11.48 3.499a.562.562 0 0 1 1.04 0l2.125 5.111a.563.563 0 0 0 .475.345l5.518.442c.499.04.701.663.321.988l-4.204 3.602a.563.563 0 0 0-.182.557l1.285 5.385a.562.562 0 0 1-.84.61l-4.725-2.885a.562.562 0 0 0-.586 0L6.982 20.54a.562.562 0 0 1-.84-.61l1.285-5.386a.562.562 0 0 0-.182-.557l-4.204-3.602a.562.562 0 0 1 .321-.988l5.518-.442a.563.563 0 0 0 .475-.345L11.48 3.5Z" />
                                </svg> }
                                Vote
                            </button>
                        </div>
                    </div>
                </div>
            ))}
        </div>
    )
}